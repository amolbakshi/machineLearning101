import numpy as np
import pandas as pd

from numpy.random import randn

np.random.seed(10)
""" Create a simple data frame """
df = pd.DataFrame(randn(5,6),['A','B','C','D','E'],[1,2,3,4,5,6])
print(df)
print("--------------------------------------------------------------------------------------------------------------------")


""" Print a series """
print(df[1])
print("--------------------------------------------------------------------------------------------------------------------")
# Can also use df.1

""" Print 2 series """
print(df[[1,2]])
print("--------------------------------------------------------------------------------------------------------------------")


""" Adding a new series to existing data frame """
df[7] = [10,20,30,40,50]
print(df)
print("--------------------------------------------------------------------------------------------------------------------")

""" Get a row from Dataframe """
print(df.loc['A'])
print(df.iloc[2])
print("--------------------------------------------------------------------------------------------------------------------")

""" Conditional selection """
print(df > 0)
print(df[df>0])
print("--------------------------------------------------------------------------------------------------------------------")

""" conditions on a column """
print(df[df[1] > 0])